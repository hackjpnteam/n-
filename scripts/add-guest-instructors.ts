async function addGuestInstructors() {
  try {
    if (!process.env.MONGODB_URI) {
      throw new Error('MONGODB_URI environment variable is required');
    }

    console.log('Connecting to MongoDB...');
    await mongoose.connect(process.env.MONGODB_URI);
    console.log('Connected to MongoDB');

    console.log(`Adding ${guestInstructors.length} guest instructors...`);
    
    // Insert the instructors
    const result = await Instructor.insertMany(guestInstructors);
    
    console.log(`Successfully added ${result.length} guest instructors to the database`);
    
    // Print some sample data
    console.log('\nSample added instructors:');
    result.slice(0, 3).forEach((instructor, index) => {
      console.log(`${index + 1}. ${instructor.name} - ${instructor.title}`);
      console.log(`   Tags: ${instructor.tags.join(', ')}`);
      console.log(`   Bio: ${instructor.bio.substring(0, 100)}...`);
      console.log('');
    });

    await mongoose.disconnect();
    console.log('Disconnected from MongoDB');
    
  } catch (error) {
    console.error('Error adding guest instructors:', error);
    await mongoose.disconnect();
    process.exit(1);
  }
}

